package main

import "fmt"

func main() {
    var a int        // default: 0
    var b string     // default: ""
    var c bool       // default: false
    var d float64    // default: 0.0

    fmt.Println("Default int:", a)
    fmt.Println("Default string:", b)
    fmt.Println("Default bool:", c)
    fmt.Println("Default float:", d)
}
